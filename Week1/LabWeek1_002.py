# Printing a line of content
print("Line1")

# Printing just a newline
print()

# Printing Content with Quotes inside
print(' "Line2"')

# Printing content with an "escaped" quote
print("\" sd")

# Printing a "tab" character
print("\tTabbed in")

# Printing content with an embedded newline
print("Line1\n    Line2")

# Printing a "Carriage Return" - It's strange and is not used often
print("ABCDEF\rXYZ")

# Printing an "escaped" backslash
print("\\")

